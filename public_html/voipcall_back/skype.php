<h2 class="mainHead">Skype</h2>
<h2 class="secHead">Staying connected with your near ones is now possible with Skype too. Use the below steps to enable VOIP calling with Skype.</h2>

<div class="divSteps"> 
    <div class="steps"> 
            <div class="st">Step 1</div>
            <p class="gtalk">Go to the <strong>Settings</strong> option in your Phone 91 account and select <strong>Add IM account</strong>. </p>
    </div>
    
    <div class="steps"> 
            <div class="st">Step2</div>
            <p class="gtalk"> Add your Skype ID.</p>
    </div>
  
    <div class="steps">
            <div class="st">Step3</div>
            <p class="gtalk">Log in to your Skype account with the Skype ID you have added and add <strong>'voip91.com'</strong> to your contacts. </p>
    </div>
    
    <div class="steps"> 
          <div class="st">Step4</div>
          <p class="gtalk">You will be added immediately. Press the <strong>call</strong> button.</p>
    </div>
    
    <div class="steps"> 
          <div class="st">Step5</div>
          <p class="gtalk">Once connected, go to the <strong>dialpad</strong> and enter the destination number where you wish to call.</p>
    </div>
    
</div>
